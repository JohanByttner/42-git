#include "libmlxutil.h"

int		mlx_cmp_points(t_point *p1, t_point *p2)
{
	if ((p1->x == p2->x
			&& p1->y == p1->y
			&& p1->z == p1->z))
		return (1);
	else
		return (0);
}
